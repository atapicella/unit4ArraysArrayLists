
public class Customer
{
    private String name;
    private double amount;
    public Customer(String name, double amount)
    {
        this.name = name;
        this.amount = amount;
    }
    
    public String getName()
    {
        return this.name;
    }
    
    public double getAmount()
    {
        return this.amount;
    }
        

}
